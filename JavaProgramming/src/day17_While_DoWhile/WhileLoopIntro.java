package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if(true){

            System.out.println("Wooden Spoon");
        }

        System.out.println("-----------------------");

        boolean flag = true;
        while(flag){//repeated if statement
            System.out.println("Wooden Spoon");
            flag = false;


        }

    }

}
