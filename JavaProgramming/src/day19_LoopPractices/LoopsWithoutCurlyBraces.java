package day19_LoopPractices;

public class LoopsWithoutCurlyBraces {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++)
            System.out.println("Cydeo");// 5 times
            System.out.println("Batch 9");// once

        System.out.println("---------------------------------------------");

        for (int i = 0; i < 5; i++) {
            System.out.println("Cydeo");// 5 times
            System.out.println("Batch 9");// 5 times



        }



    }
}
